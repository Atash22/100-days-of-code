print("Your mission is to find the treasure.")

direction = input('You have come to a crossroad. Where would you like to go? "Right" or "Left".\n').lower()
if direction == 'left':
    swim_wait = input("You have come to a lake. Would you like to swim or wait for a boat?\n").lower()
    if swim_wait == "wait":
        door = input("Which door of the boat would you like to open? Yellow, Red, or Blue\n").lower()
        if door == 'yellow':
            print("Congratulations. You have found the treasure. You win")
        elif door == 'red':
            print("Burned by fire. Game Over")
        elif door == 'blue':
            print("Eaten by beasts. Game Over")
        else:
            print("Game Over")
    else:
        print("You are attacked by a trout. Game Over")
else:
    print("Unfortunately. You have fallen into a hole, Game Over")
